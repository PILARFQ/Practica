    private void jButtonEncolarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEncolarActionPerformed
        Cliente refC = null;
        float precio =0.0f;
        String NombreDisciplina = "";
        String NombreCli = jTextFieldNombreCliente.getText();
        int Cant = Integer.parseInt(jTextFieldCantidad.getText());
        if(jComboBoxTipoDisciplinaDep.getSelectedIndex()==0){
              NombreDisciplina = "Futbol Femenino";
             precio =  35.00f;
        }else if(jComboBoxTipoDisciplinaDep.getSelectedIndex()==1){
             NombreDisciplina = "Natacion";
            precio = 30.00f;
            
        }else{
            NombreDisciplina = "Gimnasia Ritmica";
            precio = 45.00f;
        }
        refC = new Cliente(NombreCli, NombreDisciplina, precio, Cant);
        objGE.encolarCliente(refC);
        
        jTextFieldCantidad.setText("");
        jTextFieldNombreCliente.setText("");
        jComboBoxTipoDisciplinaDep.setSelectedIndex(0);
        
        
    }//GEN-LAST:event_jButtonEncolarActionPerformed

